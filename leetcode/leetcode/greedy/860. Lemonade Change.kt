    var ten = 0

    for (bill in bills) {
        when (bill) {
            5 -> five++
            10 -> {
                if (five < 1) {
                    return false
                }
                five--
                ten++
            }
            20 -> {
                if (ten > 0) {
                    if (five < 1) {
                        return false
                    }
                    ten--
                    five--
                } else {
                    if (five < 3) {
                        return false
                    }
                    five -= 3
                }
            }
        }
    }
    return true
}

